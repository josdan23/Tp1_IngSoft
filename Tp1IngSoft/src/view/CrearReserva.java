    @Override
    public void mostrarAlerta(String msj, String title, String tipo) {
        switch (tipo) {
            case "INFO":
                JOptionPane.showMessageDialog(this, msj, title,
                        JOptionPane.INFORMATION_MESSAGE);
                break;
            case "ERROR":
                JOptionPane.showMessageDialog(this, msj, title,
                        JOptionPane.ERROR_MESSAGE);
                break;
            case "ADVERTENCIA":
                JOptionPane.showMessageDialog(this, msj, title,
                        JOptionPane.WARNING_MESSAGE);
                break;
        }
    }

